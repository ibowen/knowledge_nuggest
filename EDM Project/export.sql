  CREATE OR REPLACE EDITIONABLE PROCEDURE "DBHUSTLERS"."UPDATE_NUGGET_PRICE" (this_NID nuggets.nid%type,this_cost nuggets.cost%type)
AS
cursor c1 is select c.kid,cost from contributions c join nuggets n on c.nid = n.nid where c.nid = this_nid; 
BEGIN
for res in c1 loop
update knowledge set totalcost = totalcost - res.cost + this_cost;
end loop;
update nuggets set cost = this_cost where nid = this_nid;
end;

